lista = [1,2,3,4,5]

# Acceder a un elemento por indice 

print(f"Accedemos al valor del indice 4: {lista[4]}")

# Modificar el elemento de la lista

lista[1] = 10

print(f"Accedemos al valor del indice 1: {lista[1]}")

# Agregamos un nuevo elemento a la lista

lista.append(6) # Append nos permite agregar un valor al final de la lista

print(lista)

# Eliminar elementos de la lista

del lista[2] # Con del eliminamos un valor de la lista mediante el indice

print(lista)

# Longitud de la lista

print(len(lista)) # El metodo len cuenta el largo de la lista